package br.com.guigasgame.math;


public class Rect
{
	public int left;
	public int top;
	public int width;
	public int height;

	public Rect()
	{
		left = 0;
		top = 0;
		width = 0;
		height = 0;
	}

	public Rect(int left, int top, int width, int height)
	{
		this.left = left;
		this.top = top;
		this.width = width;
		this.height = height;
	}
	
}
